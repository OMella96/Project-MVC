    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try {
            int id = Integer.parseInt(txtID.getText());
            String nombre = txtNombre.getText();
            String apellidom = txtApellidom.getText();
            String apellidop = txtApellidop.getText();
            Autor aut = new Autor(id, nombre, apellidom, apellidop);
            if (manteAutor.agregar(aut) == 1) {
                JOptionPane.showMessageDialog(this, "Agrego autor correctamente");
            } else {
                JOptionPane.showMessageDialog(this, "No Agregado");

            }
        } catch (com.mysql.jdbc.exceptions.jdbc4.CommunicationsException ex) {
            JOptionPane.showMessageDialog(this,"Error al Conectar con la BD");
        } catch (com.mysql.jdbc.exceptions.jdbc4.MySQLIntegrityConstraintViolationException ex) {
            JOptionPane.showMessageDialog(this,"No Puede agregar una PK repetida, ingrese otro dato");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

